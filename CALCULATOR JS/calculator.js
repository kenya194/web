function addition(first_num,second_num){
    var sum = first_num + second_num;
    console.log(sum);
    alert(sum);

}
function subtraction(first_num,second_num){
    var difference = first_num - second_num;
    console.log(difference);
    alert(difference);
    
}
function division(first_num,second_num){
    var divide = first_num / second_num;
    console.log(divide);
    alert(divide);
}
function multiplication(first_num,second_num){
    var multiple = first_num * second_num;
    console.log(multiple);
    alert(multiple);
}
